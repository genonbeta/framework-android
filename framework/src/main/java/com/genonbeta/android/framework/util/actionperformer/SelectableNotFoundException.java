/**
 * When the selectable is not given and it should be located, this exception may be thrown if it cannot be found.
 */
public class SelectableNotFoundException extends Exception
{
    public SelectableNotFoundException()
    {
        super();
    }

    public SelectableNotFoundException(String message)
    {
        super(message);
    }
}
